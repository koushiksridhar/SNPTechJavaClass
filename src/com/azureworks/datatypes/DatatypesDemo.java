package com.azureworks.datatypes;

public class DatatypesDemo {

    public static void main(String[] args) {

        // Prints string to console
        System.out.println("DataTypes Demo");

        // Now we are going to go over different datatypes. Below are datatypes related to numbers
        // Each of these variables have a type (int, float, double etc.). By giving a datatype,
        // it allows programs to allocate memory accordingly

        //Below are the primitive datatypes

        byte a = 23;
        System.out.println("Byte Value is " + a);

        short b = 210;
        System.out.println("Short Value is " + b);

        int c = 1233334;
        System.out.println("Int Value is " + c);

        long d = 2342344;
        System.out.println("Long Value is " + d);

        //Floats and doubles typically designate decimal numbers. Floats have to be indicated by the 'f' at
        // the end of the number. Doubles do not need indication.

        float e = 1.45f;
        System.out.println("Float Value is " + e);

        double f = 23423.5454545;
        System.out.println("Double Value is " + f);

        //Char is a character value
        char g = 'a';
        System.out.println("Char Value is " + g);






    }
}
